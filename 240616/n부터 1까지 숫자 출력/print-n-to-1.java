import java.util.*;
public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int i = n;
        while(i>=1){
            System.out.print(i + " ");
            i--;
        }
        sc.close();
        // 여기에 코드를 작성해주세요.
    }
}